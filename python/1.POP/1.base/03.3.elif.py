input_int = int(input("请输入（1-7)"))

# if后面的:,tab格式,else if 现在是elif
if input_int == 1:
    print("星期一")
elif input_int == 2:
    print("星期二")
elif input_int == 3:
    print("星期三")
elif input_int == 4:
    print("星期四")
elif input_int == 5:
    print("星期五")
elif input_int == 6:
    print("星期六")
elif input_int == 7:
    print("星期日")
else:
    print("别闹")